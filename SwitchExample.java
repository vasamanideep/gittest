package com.java;

public class SwitchExample {

	public static void main(String[] args) {
		int number=10; 
		
		switch(number){
		
		case 10:System.out.println("HI");
		
		
		break;
		case 20:System.out.println("HOW");break;
		case 12:System.out.println("you");break;
		
		
		default:System.out.println("hi how you");

	}

}
}
